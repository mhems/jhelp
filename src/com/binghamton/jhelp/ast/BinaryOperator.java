package com.binghamton.jhelp.ast;

/**
 * Enum representing valid binary operators
 */
public enum BinaryOperator {
    ADDITION,
    SUBTRACTION,
    MULTIPLICATION,
    DIVISION,
    MODULUS,
    LEFT_SHIFT,
    RIGHT_SHIFT,
    RIGHT_LOGICAL_SHIFT,
    BITWISE_AND,
    BITWISE_OR,
    BITWISE_XOR,
    AND,
    OR,
    XOR,
    EQUALITY,
    INEQUALITY,
    GREATER_THAN,
    GREATER_THAN_OR_EQUAL,
    LESS_THAN,
    LESS_THAN_OR_EQUAL,
    INSTANCE_OF
}
